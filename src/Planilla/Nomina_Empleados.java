/////////////////////////metodos enviar
public void enviarPOST(String urlStr, JSONObject json) throws Exception {
    enviarJSON(urlStr, "POST", json);
}

public void enviarPUT(String urlStr, JSONObject json) throws Exception {
    enviarJSON(urlStr, "PUT", json);
}

public void enviarJSON(String urlStr, String metodo, JSONObject json) throws Exception {
    URL url = new URL(urlStr);
    HttpURLConnection con = (HttpURLConnection) url.openConnection();

    con.setRequestMethod(metodo);
    con.setRequestProperty("Content-Type", "application/json; charset=utf-8");
    con.setDoOutput(true);

    try (OutputStream os = con.getOutputStream()) {
        byte[] input = json.toString().getBytes("utf-8");
        os.write(input, 0, input.length);
    }

    int responseCode = con.getResponseCode();
    if (responseCode != 200) {
        throw new Exception("Error en API: " + responseCode);
    }
}




