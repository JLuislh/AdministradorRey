    public void buscarEmpleado(int codigo) {
    try {
        URL url = new URL("http://40.233.19.196/nominapega/empleados/" + codigo);
        HttpURLConnection con = (HttpURLConnection) url.openConnection();

        con.setRequestMethod("GET");
        con.setRequestProperty("Accept", "application/json");

        int code = con.getResponseCode();
        if (code != 200) {
            JOptionPane.showMessageDialog(null, "No se encontró el empleado");
            return;
        }

        BufferedReader br = new BufferedReader(new InputStreamReader(con.getInputStream(), "utf-8"));
        StringBuilder response = new StringBuilder();
        String line;

        while ((line = br.readLine()) != null) {
            response.append(line.trim());
        }

        JSONObject json = new JSONObject(response.toString());

        // Llenar campos
        CODIGO.setText(json.optString("CODIGO"));
        NOMBRE.setText(json.optString("NOMBRES"));
        DPI.setText(json.optString("DPI"));
        PUESTO.setText(json.optString("PUESTO"));
        SALARIO.setText(json.optString("SALARIO"));
        BONIFICACION.setText(json.optString("BONIFICACION"));
        IGSS.setText(json.optString("IGSS"));

        // FECHA INGRESO
        String fechaIngreso = json.optString("FECHA_INGRESO");
        if (!fechaIngreso.isEmpty() && !fechaIngreso.equals("null")) {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date fecha = df.parse(fechaIngreso);
            FECHAIN.setDate(fecha);
        } else {
            FECHAIN.setDate(null);
        }

        // Checkboxes
        int tipo = json.optInt("TIPO_CONTRATACION", 0);

        if (tipo == 1) {      // PLANILLA
            chkPlanilla.setSelected(true);
            chkContrato.setSelected(false);
        } else if (tipo == 2) { // CONTRATO
            chkPlanilla.setSelected(false);
            chkContrato.setSelected(true);
        } else {
            chkPlanilla.setSelected(false);
            chkContrato.setSelected(false);
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al buscar empleado: " + e.getMessage());
    }
}
    
public void generarSiguienteCodigo() {
    try {
        URL url = new URL("http://40.233.19.196/nominapega/empleados/ultimoCodigo");
        HttpURLConnection con = (HttpURLConnection) url.openConnection();
        con.setRequestMethod("GET");

        BufferedReader br = new BufferedReader(new InputStreamReader(con.getInputStream(), "utf-8"));
        String json = br.readLine();

        JSONObject obj = new JSONObject(json);
        int ultimo = obj.optInt("ultimo", 0);

        int siguiente = ultimo + 1;

        CODIGO.setText(String.valueOf(siguiente));

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "No se pudo obtener el siguiente código");
    }
}

public void guardarNuevo() {
    try {
        JSONObject json = new JSONObject();
        json.put("CODIGO", Integer.parseInt(CODIGO.getText()));
        json.put("NOMBRES", NOMBRE.getText());
        json.put("DPI", DPI.getText());
        json.put("PUESTO", PUESTO.getText());
        json.put("SALARIO", SALARIO.getText());
        json.put("BONIFICACION", BONIFICACION.getText());
        json.put("IGSS", IGSS.getText());
        json.put("TIPO_CONTRATACION", chkPlanilla.isSelected() ? 1 : 2);
        json.put("ESTADO", 1);
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        json.put("FECHA_INGRESO", df.format(FECHAIN.getDate()));

        enviarPOST("http://40.233.19.196/nominapega/empleados", json);

        JOptionPane.showMessageDialog(null, "Empleado creado exitosamente");
        habilitarCampos(false);
        limpiarCampos();
        ListarEmpleados();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}


